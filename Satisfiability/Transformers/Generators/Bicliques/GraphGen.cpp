#include <iostream>

#include <ProgramOptions/Environment.hpp>

#include "Generators.hpp"

namespace {

  const Environment::ProgramInfo proginfo{
        "0.1.3",
        "5.3.2022",
        __FILE__,
        "Oliver Kullmann",
        "https://github.com/OKullmann/oklibrary/blob/master/Satisfiability/Transformers/Generators/Bicliques/Generators.cpp",
        "GPL v3"};

  using namespace Generators;

  const std::string error = "ERROR[" + proginfo.prg + "]: ";

  bool show_usage(const int argc, const char* const argv[]) {
    if (not Environment::help_header(std::cout, argc, argv, proginfo))
      return false;
    std::cout <<
    "> " << proginfo.prg << " graph-type ... parameters ...\n"
    " graph-type  : " << Environment::WRP<Types>{} << "\n\n"
;
    return true;
  }

}

int main(const int argc, const char* const argv[]) {

  if (Environment::version_output(std::cout, proginfo, argc, argv)) return 0;
  if (show_usage(argc, argv)) return 0;

  std::cout << create(argc, argv);

}
